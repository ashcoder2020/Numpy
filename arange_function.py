import numpy as np



#arange()
x = np.arange(1,15)                 #similar to range function in python
print(x)
x = np.arange(5)                    #gives elements from 0 to 4
print(x)
x = np.arange(1,10,2)               #gives element from 1 to 10 with difference 2
print(x)
x = np.arange(5,9,2,dtype=float)    # change element type float
print(x)
